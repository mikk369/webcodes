// Initialize language
function initLanguage() {
  var saved = localStorage.getItem('wc-lang');
  var lang = saved || 'et';
  setLanguage(lang);
}

function setLanguage(lang) {
  localStorage.setItem('wc-lang', lang);
  document.documentElement.lang = lang;

  var t = translations[lang];
  if (!t) return;

  // Update all text content
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });

  // Update all placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) {
      el.placeholder = t[key];
    }
  });

  // Update toggle button
  var toggle = document.getElementById('lang-toggle');
  if (toggle) {
    toggle.textContent = lang === 'et' ? 'EN' : 'ET';
    toggle.setAttribute('aria-label', lang === 'et' ? 'Switch to English' : 'Lülitu eesti keelele');
  }
}

document.addEventListener('DOMContentLoaded', function () {
  initLanguage();

  var toggle = document.getElementById('lang-toggle');
  if (toggle) {
    toggle.addEventListener('click', function () {
      var current = localStorage.getItem('wc-lang') || 'et';
      setLanguage(current === 'et' ? 'en' : 'et');
    });
  }
});
